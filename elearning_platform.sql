    e.user_id,
    e.course_id,
    e.completion_status,
    c.rating,
    c.price,
    c.category,
    DATEDIFF(e.enrollment_date, u.join_date) AS days_since_joined,
    DATEDIFF('2025-05-28', e.enrollment_date) AS days_since_enrolled
FROM enrollments e
JOIN courses c ON e.course_id = c.course_id
JOIN users u ON e.user_id = u.user_id;